package br.com.estadio.model;

public class Ingresso {

    private int idIng;
    private int clientelaIng;
    private int cadeiroteIng;
    private int diajIng;

    public int getIdIng() {
        return idIng;
    }

    public void setIdIng(int idIng) {
        this.idIng = idIng;
    }

    public int getClientelaIng() {
        return clientelaIng;
    }

    public void setClientelaIng(int clientelaIng) {
        this.clientelaIng = clientelaIng;
    }

    public int getCadeiroteIng() {
        return cadeiroteIng;
    }

    public void setCadeiroteIng(int cadeiroteIng) {
        this.cadeiroteIng = cadeiroteIng;
    }

    public int getDiajIng() {
        return diajIng;
    }

    public void setDiajIng(int diajIng) {
        this.diajIng = diajIng;
    }

}
